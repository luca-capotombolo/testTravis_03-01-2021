package com.logic.Model;

public class EsameDato {
	
	private final String nome;
	private final String voto;
	
	public String getNome() {
		return nome;
	}

	public String getVoto() {
		return voto;
	}

	public EsameDato(String nome, String voto) {
		this.nome = nome;
		this.voto = voto;
	}	
	
	
}
